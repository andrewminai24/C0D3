/**
 * Write a function called solution that
 * Takes in 2 numbers, return true if the
 *    first number is greater than the second
 *    and false otherwise
 * @param {number} a
 * @param {number} b
 * @returns {boolean}
 */

const solution = (a, b) => {
  if (a > b) {
    return true;
  } else {
    return false;
  }
};
module.exports = {
  solution
};
